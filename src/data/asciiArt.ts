// Helper function to get enemy art based on state
export function getEnemyArt(enemyId: string, state: 'idle' | 'hurt' | 'attack' = 'idle'): string {
    const art = ENEMY_ASCII_ART[enemyId];
    if (!art) {
        return `
     â“
    â•±â”€â•²
   â”‚ ? ?â”‚
   â”‚  ?  â”‚
    â•²â”€â•±
     â”‚
    â•±â”‚â•²`;
    }

    if (state === 'hurt' && art.hurt) {
        return art.hurt;
    } else if (state === 'attack' && art.attack) {
        return art.attack;
    }

    return art.idle;
}

// Helper to get random attack animation
